package org.lingxi.youxi.collect.beans;

/**
 * 辅助信息
 * @author Administrator
 * 2015年9月19日
 */
public class Auxiliary {

	private String name;
	private String level;//等级
	private String profession;//职业
	private Integer killedCount;//被杀次数
	private Integer baoshi;//暴尸
	private Integer relive;//复活
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the level
	 */
	public String getLevel() {
		return level;
	}
	/**
	 * @param level the level to set
	 */
	public void setLevel(String level) {
		this.level = level;
	}
	/**
	 * @return the profession
	 */
	public String getProfession() {
		return profession;
	}
	/**
	 * @param profession the profession to set
	 */
	public void setProfession(String profession) {
		this.profession = profession;
	}
	/**
	 * @return the killedCount
	 */
	public Integer getKilledCount() {
		return killedCount;
	}
	/**
	 * @param killedCount the killedCount to set
	 */
	public void setKilledCount(Integer killedCount) {
		this.killedCount = killedCount;
	}
	/**
	 * @return the baoshi
	 */
	public Integer getBaoshi() {
		return baoshi;
	}
	/**
	 * @param baoshi the baoshi to set
	 */
	public void setBaoshi(Integer baoshi) {
		this.baoshi = baoshi;
	}
	/**
	 * @return the relive
	 */
	public Integer getRelive() {
		return relive;
	}
	/**
	 * @param relive the relive to set
	 */
	public void setRelive(Integer relive) {
		this.relive = relive;
	}
	
}
