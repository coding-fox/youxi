public class Comprehensive {

	private String name;//名称
	private String level;//等级
	private String profession;//职业
	private Integer kill;//杀人
	private Integer assists;//助攻
	private Integer repair;//维修
	private Integer accumulate;//积分
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the level
	 */
	public String getLevel() {
		return level;
	}
	/**
	 * @param level the level to set
	 */
	public void setLevel(String level) {
		this.level = level;
	}
	/**
	 * @return the profession
	 */
	public String getProfession() {
		return profession;
	}
	/**
	 * @param profession the profession to set
	 */
	public void setProfession(String profession) {
		this.profession = profession;
	}
	/**
	 * @return the kill
	 */
	public Integer getKill() {
		return kill;
	}
	/**
	 * @param kill the kill to set
	 */
	public void setKill(Integer kill) {
		this.kill = kill;
	}
	/**
	 * @return the assists
	 */
	public Integer getAssists() {
		return assists;
	}
	/**
	 * @param assists the assists to set
	 */
	public void setAssists(Integer assists) {
		this.assists = assists;
	}
	/**
	 * @return the repair
	 */
	public Integer getRepair() {
		return repair;
	}
	/**
	 * @param repair the repair to set
	 */
	public void setRepair(Integer repair) {
		this.repair = repair;
	}
	/**
	 * @return the accumulate
	 */
	public Integer getAccumulate() {
		return accumulate;
	}
	/**
	 * @param accumulate the accumulate to set
	 */
	public void setAccumulate(Integer accumulate) {
		this.accumulate = accumulate;
	}
	
	
}
